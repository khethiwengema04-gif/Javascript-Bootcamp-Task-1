// Variable Reassignment

//  Declare a variable 'favoriteColour'
let favoriteColour = "Blue";

//  Print the value of favoriteColour
console.log("Favourite Colour:", favoriteColour);

//  Reassign favoriteColour to a different colour
favoriteColour = "Green";

//  Print the new value of favoriteColour
console.log("New Favourite Colour:", favoriteColour);


